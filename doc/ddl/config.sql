create database config character set utf8 collate utf8_bin;

 CREATE TABLE config.OBJECT (
  ID INT NOT NULL AUTO_INCREMENT,
  NAME varchar(128) NOT NULL,
  PRIMARY KEY (ID)
 )ENGINE = InnoDB  DEFAULT CHARSET=utf8;
 
CREATE TABLE config.POSITION_RELATION (
  ID INT NOT NULL,
  OBJECT_TYPE INT NOT NULL,
  PARENT_ID INT,
  PRIMARY KEY (ID),
  FOREIGN KEY (ID) REFERENCES config.OBJECT (ID),
  FOREIGN KEY (PARENT_ID) REFERENCES config.OBJECT (ID) on delete CASCADE
 )ENGINE = InnoDB  DEFAULT CHARSET=utf8;
 
CREATE TABLE config.ADMINISTRATIVE_REGION(
	ID INT NOT NULL,
  	NAME varchar(128) NOT NULL,
  	ABBREVIATION varchar(64) NOT NULL,
  	ZIP_CODE varchar(12) NOT NULL,
  	LONGITUDE FLOAT,
  	LATITUDE FLOAT,
  PRIMARY KEY (ID),
  FOREIGN KEY (ID) REFERENCES config.OBJECT (ID) on delete CASCADE
)ENGINE = InnoDB  DEFAULT CHARSET=utf8;

CREATE TABLE config.BUILDING(
	ID INT NOT NULL,
  	NAME varchar(128) NOT NULL,
  	CODE varchar(32) NOT NULL,
  	PRIMARY KEY (ID),
  	FOREIGN KEY (ID) REFERENCES config.OBJECT (ID) on delete CASCADE
)ENGINE = InnoDB  DEFAULT CHARSET=utf8;

CREATE TABLE config.ROOM(
	ID INT NOT NULL,
  	NAME varchar(128) NOT NULL,
  	CODE varchar(32) NOT NULL,
  	ROOM_TYPE INT NOT NULL,
  	PRIMARY KEY (ID),
  	FOREIGN KEY (ID) REFERENCES config.OBJECT (ID) on delete CASCADE
)ENGINE = InnoDB  DEFAULT CHARSET=utf8;
